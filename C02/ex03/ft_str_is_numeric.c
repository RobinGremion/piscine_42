int	ft_str_is_numeric(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			j = 0;
		i ++;
	}
	return (j);
}
